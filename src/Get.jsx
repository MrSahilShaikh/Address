const ContractAddress = '0xB940db20C8A97efbA110F4eaa607B2029C3aa441'; // Replace with your contract address

const ContributionDetails = () => {
    const [web3, setWeb3] = useState(null);
    const [contract, setContract] = useState(null);
    const [account, setAccount] = useState('');
    const [address, setAddress] = useState('');
    const [amounts, setAmounts] = useState({});
    const [isLoading, setIsLoading] = useState(false);

    // Connect to MetaMask
    const connectMetaMask = async () => {
        if (window.ethereum) {
            try {
                await window.ethereum.request({ method: 'eth_requestAccounts' });
                const web3Instance = new Web3(window.ethereum);
                setWeb3(web3Instance);
                const contractInstance = new web3Instance.eth.Contract(
                    usdtraisingContractABI,
                    ContractAddress
                );
                setContract(contractInstance);
                const accounts = await web3Instance.eth.getAccounts();
                setAccount(accounts[0]);
            } catch (error) {
                console.error(error);
            }
        } else {
            alert('MetaMask not installed');
        }
    };

    // Function to fetch contribution amounts for all addresses
    const getAllContributorAmounts = async () => {
        if (!contract || !web3 || isLoading) return;

        setIsLoading(true);

        try {
            const contributorsCount = await contract.methods.numOfContributors().call();
            const amounts = {};
            for (let i = 0; i < contributorsCount; i++) {
                const contributorAddress = await contract.methods.contributorsList(i).call();
                const weiAmount = await contract.methods.contributions(contributorAddress).call({ from: account });
                const etherAmount = web3.utils.fromWei(weiAmount, 'ether');
                amounts[contributorAddress] = etherAmount.toString(); // Convert to string
            }
            setAmounts(amounts);
        } catch (error) {
            console.error('Error fetching contributor amounts:', error);
        } finally {
            setIsLoading(false);
        }
    };

    // CSV data for ReactCSV
    const csvData = Object.entries(amounts).map(([address, amount]) => ({
        Address: address,
        'Contribution Amount (Ether)': amount,
    }));

    return (
        <div>
            <div style={{ maxWidth: '800px', margin: '0 auto' }}>
                <header style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '20px' }}>
                    <div style={{ display: 'flex', alignItems: 'center' }}>
                        <p style={{ marginRight: '10px', fontWeight: 'bold' }}>Connected Wallet Address: {account}</p>
                        <button style={{ padding: '5px 10px', backgroundColor: '#007bff', color: '#fff', border: 'none', borderRadius: '3px', cursor: 'pointer' }} onClick={connectMetaMask}>Connect MetaMask</button>
                    </div>
                    {/* Download button */}
                    <CSVLink data={csvData} filename={"contributions.csv"}>
                        <button style={{ padding: '5px 10px', backgroundColor: '#28a745', color: '#fff', border: 'none', borderRadius: '3px', cursor: 'pointer' }} disabled={Object.keys(amounts).length === 0}>
                            Download CSV
                        </button>
                    </CSVLink>
                </header>

                <div style={{ padding: '20px', border: '1px solid #ccc', borderRadius: '5px' }}>
                    <button style={{ marginBottom: '10px', padding: '5px 10px', backgroundColor: '#007bff', color: '#fff', border: 'none', borderRadius: '3px', cursor: 'pointer' }} onClick={getAllContributorAmounts} disabled={!contract || !web3 || isLoading}>Get All Contributor Amounts</button>
                    {isLoading && <p>Loading...</p>}
                    <div>
                        {Object.keys(amounts).length > 0 && (
                            <div>
                                <h2>All Contributor Amounts:</h2>
                                <ul>
                                    {Object.entries(amounts).map(([address, amount], index) => (
                                        <li key={index}>
                                            <p>Address: {address}</p>
                                            <p>Contribution Amount: {amount} Ether</p>
                                        </li>
                                    ))}
                                </ul>
                            </div>
                        )}
                    </div>
                </div>
            </div>
        </div>
    );
};

export default ContributionDetails;
